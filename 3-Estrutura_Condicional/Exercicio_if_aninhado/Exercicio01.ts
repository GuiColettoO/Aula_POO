const numero: number = -10;

if(numero > 0){
  console.log("O número é positivo");
  if(numero % 2 == 0){
    console.log("O numero é par");
  } else {
    console.log("O numero é impar");
  }
} else {
  console.log("O número é negativo");
  if(numero % 2 == 0){
    console.log("O numero é par");
  } else {
    console.log("O numero é impar");
  }
}
